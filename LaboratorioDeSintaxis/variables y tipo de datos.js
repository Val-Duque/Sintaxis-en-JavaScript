// usa const, es para datos que no cambia. Y el let es para poderla cambiar 

const nombre = "Val";
let edad = 21;
const correoElectronico = "val@duque.com";
const estadoActivo = True 

// typeof muestra el tipo de informacion 

console.log ({
    nombre: nombre,
    tipoNombre: typeof nombre,

    edad: edad,
    tipoEdad: typeof edad,

    correoElectronico: correoElectronico,
    tipoCorreo: typeof correoElectronico,

    estadoActivo: typeof estadoActivo
})


/* 

Como usa:

Que declarar las 4 variables: nombre, edad, correo y estado activo.

Como declararlas usar let para valores que pueden cambiar, const para los que no.

Que mostrar el tipo de dato con typeof.

Como mostrarlo  un solo console.log() con toda la info.

*/